
/*Final Keyword
 * - You can declare variable, method & class as final.
 * - If you declare variable as a final then you cannot change it's value.
 * - If you declare method as a final then you cannot override it.
 * - If you declare class as a final then you cannot extend it.
 * 
 * */

class f1
{

	final int a;
	
	f1() {
		a=10;
	}
	void show()
	{
	}
}
class f2 extends f1
{
	void show()
	{
		
	}
}
